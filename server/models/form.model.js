import mongoose, { Schema } from "mongoose";

const FormSchema = new Schema({
  fullName: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  pincode: {
    type: String,
    required: true,
  },
});

const formModel = mongoose.model("form", FormSchema);

export default formModel;
